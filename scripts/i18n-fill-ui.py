def apply(path: pathlib.Path, patch: dict) -> None:
    data = json.loads(path.read_text(encoding="utf-8"))
    for ns, value in patch.items():
        data[ns] = value
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"updated {path.name}: {', '.join(patch.keys())}")


